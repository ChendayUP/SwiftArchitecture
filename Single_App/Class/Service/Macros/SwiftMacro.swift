import Foundation
import UIKit

public func ClassFromString(_ aClassName: String) ->Swift.AnyClass? {
    let bundleName = Bundle.main.object(forInfoDictionaryKey: "CFBundleName") as! String;
    return NSClassFromString("\(bundleName).\(aClassName)");
}
